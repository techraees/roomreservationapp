import mongoose from "mongoose";
const { Schema } = mongoose;

// Declare the Schema of the Mongo model
var UserSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

//Export the model
export default mongoose.model("User", UserSchema);
